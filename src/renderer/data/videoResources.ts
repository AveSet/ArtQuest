export function videoResourceMatchesFilters(
  r: VideoResource,
  opts: {
    category: QuestCategory | 'all'
    nodeId: string | null
    tag: string | null
    search: string
  },
): boolean {
  const q = (opts.search ?? '').trim().toLowerCase()
  if (opts.category !== 'all' && r.category !== opts.category) return false
  if (opts.nodeId && !r.nodeIds.includes(opts.nodeId)) return false
  if (opts.tag) {
    const wanted = opts.tag.trim().toLowerCase()
    if (!r.tags.some((t) => t.toLowerCase() === wanted)) return false
  }
  if (!q) return true
  const blob = `${r.titleEn} ${r.titleRu} ${r.tags.join(' ')}`.toLowerCase()
  return blob.includes(q)
}

export function filterVideoResources(
  catalog: VideoResource[],
  opts: {
    category: QuestCategory | 'all'
    nodeId: string | null
    tag: string | null
    search: string
  },
): VideoResource[] {
  return catalog.filter((r) => videoResourceMatchesFilters(r, opts))
}

/** Max unique YouTube videos shown in Materials catalog (deduped by `youtubeId`). */
export const RESOURCE_CATALOG_DISPLAY_LIMIT = 30

/** Hand-picked entries in `CORE_VIDEO_RESOURCES` (vs auto YouTube search imports). */
export function isCoreCatalogVideo(resource: VideoResource): boolean {
  return !resource.id.startsWith('auto-yt-')
}

/** First occurrence wins (respects incoming sort, e.g. favorites first). */
export function dedupeVideoResourcesByYoutubeId(resources: VideoResource[]): VideoResource[] {
  const seen = new Set<string>()
  const out: VideoResource[] = []
  for (const r of resources) {
    const key = r.youtubeId.trim() ? r.youtubeId : r.id
    if (seen.has(key)) continue
    seen.add(key)
    out.push(r)
  }
  return out
}

export function tagsForCategory(catalog: VideoResource[], category: QuestCategory | 'all'): string[] {
  const set = new Set<string>()
  for (const r of catalog) {
    if (category !== 'all' && r.category !== category) continue
    for (const t of r.tags) set.add(t)
  }
  return [...set].sort()
}
